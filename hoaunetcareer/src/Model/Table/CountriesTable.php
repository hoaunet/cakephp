{

    /**
     * Initialize method
     *
     * @param array $config The configuration for the Table.
     * @return void
     */
    public function initialize(array $config)
    {
        parent::initialize($config);

        $this->setTable('countries');
        $this->setDisplayField('id');
        $this->setPrimaryKey('id');

        
        $this->hasMany('Provinces', [
            'foreignKey' => 'country_id'
        ]);
    }

    /**
     * Default validation rules.
     *
     * @param \Cake\Validation\Validator $validator Validator instance.
     * @return \Cake\Validation\Validator
     */
    public function validationDefault(Validator $validator)
    {
        $validator
            ->integer('id')
            ->allowEmpty('id', 'create');

        $validator
            ->requirePresence('countries_name', 'create')
            ->notEmpty('countries_name');

        $validator
            ->requirePresence('countries_iso_code_2', 'create')
            ->notEmpty('countries_iso_code_2');

        $validator
            ->requirePresence('countries_iso_code_3', 'create')
            ->notEmpty('countries_iso_code_3');

        return $validator;
    }

